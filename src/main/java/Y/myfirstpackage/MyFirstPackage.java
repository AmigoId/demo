package Y.myfirstpackage;

public class MyFirstPackage {
	
	private int y;
	private int x;

	public MyFirstPackage(int y, int x) {
		this.y = y;
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}
}



